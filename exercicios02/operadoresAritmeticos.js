
function soma(a, b) {
    return a + b;
}

function subtrai(a, b) {
    return a - b;
}

function multiplica(a, b) {
    return a * b;
}

function divisao(a, b) {
    return a / b;
}

console.log(soma(10, 5))
console.log(subtrai(10, 5));
console.log(multiplica(10, 5));
console.log(divisao(10, 5));


