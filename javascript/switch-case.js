let fruta = "BanAnA";

switch (fruta.toLowerCase()) {
    case "laranja":
        console.log("Suco de Laranja!")
        break;

    case "banana":
        console.log("Vitamina de Banana!")
        break;

    case "maça":
        console.log("Suco de Maça!")
        break;

    default:
    console.log("Suco genérico!");
}